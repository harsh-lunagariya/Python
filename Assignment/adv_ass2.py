n=11
size = n*3
mid = n//2
ct=1
for i in range(n):
    if i==mid :
        temp = (size-3)//2
        print('-'*temp,end='')
        print('AUM',end='')
        print('-'*temp)
    
    elif i<mid:
        temp = (size-(3*(ct))-1)//2
        print('-'*temp,end='')
        print('/',end='')
        print('.|.'*(ct),end='')
        print('\\',end='')
        print('-'*temp)
        ct +=2
    else:
        ct -=2
        temp = (size-(3*(ct))-1)//2
        print('-'*temp,end='')
        print('\\',end='')
        print('.|.'*(ct),end='')
        print('/',end='')
        print('-'*temp)

